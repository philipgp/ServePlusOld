package com.splus.pojo;

public class Role {
	
	private byte roleId;
	private String roleName;
	
	Role(){}

	public byte getRoleId() {
		return roleId;
	}

	public void setRoleId(byte roleId) {
		this.roleId = roleId;
	}

	public String getRoleName() {
		return roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

}
